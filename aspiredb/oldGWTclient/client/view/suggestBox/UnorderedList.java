package ubc.pavlab.aspiredb.client.view.suggestBox;

import com.google.gwt.user.client.DOM;
import com.google.gwt.user.client.ui.ComplexPanel;
import com.google.gwt.user.client.ui.Widget;

/**
 * This widget represents HTML <ul> elements.
 * 
 * @version $Id: UnorderedList.java,v 1.1 2013/04/19 06:51:20 frances Exp $
 */
public class UnorderedList extends ComplexPanel {
	public UnorderedList() {
		setElement(DOM.createElement("UL"));
	}

	public void add(Widget w) {
		super.add(w, getElement());
	}

	public void insert(Widget w, int beforeIndex) {
		super.insert(w, getElement(), beforeIndex, true);
	}
}

